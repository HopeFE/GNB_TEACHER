<template>
  <view-box  class="passList">
    <div slot="header" style="position:absolute;left:0;top:0;width:100%;z-index:100">
      <x-header :left-options="{showBack: true}">
          弃题列表
         <a slot="right" @click="_intoBan"><i class="icon iconfont icon-ban" style="font-size:22px"></i></a>
      </x-header>
      <header v-if="passList" class="sectionHeader">
        <p class="ellipsis">{{passList.chapterName}}</p>
        <font class="ellipsis">共<b>{{passList.count}}</b>个题型</font>
      </header>
    </div>
    <!--空白间隔-->
    <div style="padding-top:80px;">
      <template v-if="passList">
      <div class="weui_panel weui_panel_access exerciseExampleList" v-for="item in passList.list">
        <div class="weui_panel_hd">
          {{{item.chapter_name}}}
        </div>
        <div @click="_intoDetail(item.excercise_id)"> 
          <div class="weui_panel_bd">
            <a class="weui_media_box weui_media_appmsg" >
              <div class="weui_media_bd">
                <p class="example_title">参考例题<b>难度：{{item.degree}}</b></p>
                <p class="weui_media_desc">
                  {{{item.stem}}}
                </p>
              </div>
            </a>
          </div>
          <div v-if=" item.opt_jo.hasOwnProperty('A')"  class="weui_media_bd weui_media_box options">
              <p class="weui_media_desc" v-for="value in item.opt_jo">
                {{ $key }} : {{{ value }}}
              </p>
          </div>
        </div>
        <div class="abandon">
          <span @click="_abandon(item.loose_win_excercise_id,$index)">撤回</span>
        </div>
      </div>
      </template>
      <infinite-loading :on-infinite="_onInfinite" spinner="waveDots" style="height:60px">
        <span slot="no-results" style="color:#4bb7aa;">
          <i class="icon iconfont icon-comiiszanwushuju" style="font-size:1.5rem;margin-right:.2rem"></i>
          <p style="font-size:1rem;display:inline-block;">您没有错题~</p>
        </span>
        <span slot="no-more" style="color:#4bb7aa;font-size:.8rem;">已加载全部</span>
      </infinite-loading>
    </div>
  </view-box>

</template>

<script>
import {XHeader,Panel,Flexbox,FlexboxItem,XButton,ViewBox,ButtonTab,ButtonTabItem} from 'vux'
import InfiniteLoading from 'vue-infinite-loading'
import { mapActions,mapGetters  } from 'vuex'

export default {
  components: {
    XHeader,XButton,InfiniteLoading,
    Panel,Flexbox,FlexboxItem,ViewBox,ButtonTab,ButtonTabItem
  },
  route: {
    data:function(transition){
      /**
      * 标志为reset则重置，否则加载上次高度
      */
      if(this.passListIsReset){
        this.$nextTick(() => {
          this.$broadcast('$InfiniteLoading:reset');
        })
      }else{
        this.$nextTick(() => {
          document.getElementsByClassName("vux-fix-safari-overflow-scrolling")[0].scrollTop = this.passListScroll;
        })
      }
    }
  },
  methods: {
    ...mapActions(['getPassList','setPassListScroll','passAction']),
    _intoBan(){
      history.go(-2);
    },
    //撤回动作
    _abandon(id,index){
      this.passAction({
        "id":id,
        "index":index
      });
    },
    _intoDetail(id){
      this.setPassListScroll(document.getElementsByClassName("vux-fix-safari-overflow-scrolling")[0].scrollTop);
      this.$router.go(`/example/${this.Params.studentId}/${this.passSubjectId}/${id}`);
    },
    _onInfinite(){
      this.getPassList()
      .then((res)=>{
        this.$broadcast('$InfiniteLoading:loaded');
        let length = Number(res.data.data.list.length);
        if(length  < 5){
            this.$broadcast('$InfiniteLoading:complete');
            return;
        }
      })
    }
  },
  computed:{
    ...mapGetters(['passList','passListIsReset','passSubjectId','passListScroll','Params'])
  }
}
</script>
